# Checking operations
print(3 + 4)             # addition(+)
print(4 - 3)             # subtraction(-)
print(3 * 4)             # multiplication(*)
print(4 / 3)             # division(/)
print(4 ** 3)            # exponential(**)
print(4 % 3)             # modulus(%)
print(4 // 3)            # floor division operator(//)

# Checking strings
print('Alex')            # name
print('Guerrero')        # family name
print('USA')             # country
print('I am enjoying 30 days of python') # sentence

# Checking data types
print(type(10))          # Int
print(type(9.8))         # float
print(type(3.14))        # Float
print(type(4 - 4j))      # Complex number
print(type([1, 2, 3]))   # list
print(type({'name':'Alex'})) # dictionary
print(type({9.8, 3.14, 2.7}))    # set
print(type((9.8, 3.14, 2.7)))    # tuple
print(type('Alex'))      # string
print(type('Guerrero'))
print(type('USA'))
print(type('I am enjoying 30 days of python'))

